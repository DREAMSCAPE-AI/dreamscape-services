// Airport code aliases to canonical city code
const CITY_CODE_ALIASES: Record<string, string> = {
  CDG: 'PAR', ORY: 'PAR',
  JFK: 'NYC', LGA: 'NYC', EWR: 'NYC',
  LHR: 'LON', LGW: 'LON', STN: 'LON',
  NRT: 'TYO', HND: 'TYO',
  FCO: 'ROM', CIA: 'ROM',
  MXP: 'MIL', LIN: 'MIL',
  BUR: 'LAX',
  ORD: 'CHI', MDW: 'CHI',
  IAD: 'WAS', DCA: 'WAS',
  YYZ: 'YTO',
  GRU: 'SAO',
  GIG: 'RIO',
};

/**
 * Get hotel fixtures for a given cityCode (handles airport code aliases).
 */
export function getHotelFixtures(cityCode: string): HotelFixture[] {
  if (!cityCode) return [];
  const normalized = cityCode.toUpperCase();
  const canonical = CITY_CODE_ALIASES[normalized] || normalized;
  return HOTEL_FIXTURES.filter(h => h.cityCode === canonical || h.cityCode === normalized);
}

/**
 * Generate a realistic nightly rate based on star rating and check-in date.
 */
export function generateNightlyRate(hotel: HotelFixture, checkInDate: string): { amount: string; currency: string } {
  const BASE_PRICES: Record<number, [number, number]> = {
    5: [350, 800],
    4: [150, 350],
    3: [80, 150],
    2: [50, 90],
    1: [30, 60],
  };
  const [min, max] = BASE_PRICES[hotel.stars] || [80, 200];
  const seed = hotel.id.charCodeAt(hotel.id.length - 1) + (checkInDate ? parseInt(checkInDate.replace(/-/g, '')) % 100 : 0);
  const amount = min + (seed % (max - min));
  return { amount: amount.toFixed(2), currency: 'EUR' };
}
